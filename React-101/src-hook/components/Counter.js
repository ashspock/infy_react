import React, {Component} from 'react';
export default class CounterClass extends Component {
  state = { count: 0 };
  render() {
    return (
      <div>
        <p>You clicked {this.state.count} times</p>
        <button onClick={() => this.setState({ count: this.state.count + 1 })}>
          Click me
        </button>
      </div>
    );
  }
}
